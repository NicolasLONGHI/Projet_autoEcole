    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        lblTitre.setText("Bienvenue " + unUser.getPrenom());
        
        
        dcDebut.setDateFormatString("dd/MM/yyyy");
        dcFin.setDateFormatString("dd/MM/yyyy");
        java.util.Date date = new java.util.Date();
        Calendar calendar = Calendar.getInstance();
        calendar.add(Calendar.DATE, -7);
        dcDebut.setDate(calendar.getTime());
        dcFin.setDate(date);
        
        ImageIcon icon = new ImageIcon(getClass().getResource("/Images/deconnexion.png"));
        Image img = icon.getImage();
        img = img.getScaledInstance(lblLogoDeconnexion.getWidth(), lblLogoDeconnexion.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon ic = new ImageIcon(img);
        lblLogoDeconnexion.setIcon(ic);
    }//GEN-LAST:event_formWindowOpened

